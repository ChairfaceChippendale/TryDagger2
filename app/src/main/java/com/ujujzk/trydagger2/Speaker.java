package com.ujujzk.trydagger2;


public class Speaker {
    private String name;
    private String speech;

    public void setName(String name) {
        this.name = name;
    }

    public void setSpeech(String speech) {
        this.speech = speech;
    }

    public String getName() {
        return name;
    }

    public String getSpeech() {
        return speech;
    }
}
